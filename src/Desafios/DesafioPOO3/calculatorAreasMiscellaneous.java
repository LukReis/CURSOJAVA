package Desafios.DesafioPOO3;

public class calculatorAreasMiscellaneous {

    static final double PI = 3.14159265358979323846;

    public static double areaOfSquare(double side) {
        return side * side;
    }

    public static double areaOfCirculo(double ray) {
        return (ray * ray) * PI;
    }

}
